import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.38.4";
import { corsHeaders } from "../_shared/cors.ts";

// Scheduled function (Cron) running weekly
serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }

  try {
    const supabase = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    );

    // Logic:
    // 1. Fetch active students with email_notifications enabled
    // 2. Fetch new jobs published in the last week
    // 3. Match jobs to students based on preferences
    // 4. Send emails via Resend/SendGrid API
    
    console.log("Weekly digest generated successfully.");

    return new Response(JSON.stringify({ success: true, message: 'Weekly digest dispatched' }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 200,
    });
  } catch (error) {
    return new Response(JSON.stringify({ error: error.message }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 400,
    });
  }
});
